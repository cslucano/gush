namespace Gush\Command;

use Symfony\Component\Console\Input\InputArgument;
use Symfony\Component\Console\Input\InputInterface;
use Symfony\Component\Console\Output\OutputInterface;
use Gush\Feature\GitHubFeature;

/**
 * Random image generator
 *
 * @author Luis Cordova <cordoval@gmail.com>
 */
class PullRequestImageGeneratorCommand extends BaseCommand implements GitHubFeature
{
    /**
     * {@inheritdoc}
     */
    protected function configure()
    {
        $this
            ->setName('pull-request:image-generator')
            ->setDescription('Plugs an image on a pull request')
            ->addArgument('pr_number', InputArgument::REQUIRED, 'Pull request number')
            ->setHelp(
                <<<EOF
The <info>%command.name%</info> plugs random image on a pull request:

    <info>$ gush %command.full_name% 12</info>
EOF
            )
        ;
    }

    /**
     * {@inheritdoc}
     */
    protected function execute(InputInterface $input, OutputInterface $output)
    {
        $org = $input->getOption('org');
        $repo = $input->getOption('repo');

        $prNumber = $input->getArgument('pr_number');

        $client = $this->getGithubClient();

        $items = ['coffee', 'zombie', 'bacon', 'beer'];
        $item = $items[array_rand($items)];
        $imageMessage = constant('Gush\Command\Images::'.$item);
        $parameters = ['body' => $imageMessage];
        $client->api('issue')->comments()->create($org, $repo, $prNumber, $parameters);

        $output->writeln("Image posted to https://github.com/{$org}/{$repo}/pull/{$prNumber}");

        return self::COMMAND_SUCCESS;
    }
}

